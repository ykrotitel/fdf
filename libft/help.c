#include "../rainbow.h"

int		ft_takewith(char *s)
{
	int i;

	i = 0;
	while(*s != '\0')
	{
		if (*s >= '0' && *s <= '9')
		{
			while (*s >= '0' && *s <= '9')
				s++;
			i++;
		}
		s++;
	}
	return (i);
}

int		ft_howlong(int a)
{
	int i;

	i = (a > 0 ? 0 : 1);
	while (a)
	{
		a /= 10;
		i++;
	}
	return (i);
}